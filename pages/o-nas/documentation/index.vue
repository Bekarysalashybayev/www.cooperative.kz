        должностных лиц, возмещается в порядке, установленном законодательством Республики Казахстан.</p>
      <strong>
        <p class="mb-4">Глава 2. Создание потребительского кооператива</p>
      </strong>
      <strong>
        <p class="mb-2">Статья 9. Порядок создания потребительского кооператива</p>
      </strong>
      <p class="mb-2">1. Создание потребительского кооператива начинается с проведения учредительного собрания и
        завершается государственной регистрацией в качестве юридического лица.</p>
      <p class="mb-4">2. Граждане могут образовывать инициативные группы по подготовке проведения учредительного
        собрания потребительского кооператива и проектов документов, выносимых на его рассмотрение.</p>
      <strong>
        <p class="mb-2">Статья 10. Учредительное собрание потребительского кооператива</p>
      </strong>
      <p class="mb-2">1. Учредительное собрание потребительского кооператива решает следующие вопросы:</p>
      <p class="mb-2 ml-3">1) принимает решение о создании потребительского кооператива, его наименовании и месте
        нахождения;</p>
      <p class="mb-2 ml-3">2) утверждает список членов потребительского кооператива на основании их заявлений с
        указанием фамилии, имени, отчества, места жительства и данных документа, удостоверяющего личность, для граждан и
        с указанием наименования, места нахождения и банковских реквизитов для юридического лица;</p>
      <p class="mb-2 ml-3">3) определяет предмет и цели деятельности;</p>
      <p class="mb-2 ml-3">4) определяет состав, размер и сроки внесения имущественных (паевых) взносов;</p>
      <p class="mb-2 ml-3">5) утверждает устав и учредительный договор;</p>
      <p class="mb-2 ml-3">6) избирает исполнительный, контрольный и иные органы;</p>
      <p class="mb-2 ml-3">7) определяет сроки и лиц, ответственных за государственную регистрацию;</p>
      <p class="mb-2 ml-3">8) решает иные вопросы, связанные с созданием потребительского кооператива.</p>
      <p class="mb-4">2. Решение учредительного собрания оформляется протоколом. Протокол учредительного собрания
        подписывается председателем и секретарем учредительного собрания. Председатель учредительного собрания
        избирается большинством голосов его участников.</p>
      <strong>
        <p class="mb-2">Статья 11. Устав потребительского кооператива</p>
      </strong>
      <p class="mb-2">1. Устав потребительского кооператива должен содержать:</p>
      <p class="mb-2 ml-3">1) наименование и местонахождение;</p>
      <p class="mb-2 ml-3">2) предмет и цели деятельности;</p>
      <p class="mb-2 ml-3">3) порядок вступления и выхода членов из потребительского кооператива, в том числе выдела
        своей доли в имуществе кооператива;</p>
      <p class="mb-2 ml-3">4) состав, размер и порядок внесения имущественных (паевых) взносов, ответственность за
        нарушение обязательств по внесению имущественных (паевых) взносов;</p>
      <p class="mb-2 ml-3">5) порядок формирования, избрания (переизбрания) и полномочия исполнительного, контрольного и
        иных органов потребительского кооператива, порядок принятия ими решений;</p>
      <p class="mb-2 ml-3">6) состав и компетенцию органов управления потребительского кооператива;</p>
      <p class="mb-2 ml-3">7) порядок принятия решений органами управления, в том числе о вопросах, решения по которым
        принимаются единогласно или квалифицированным большинством голосов;</p>
      <p class="mb-2 ml-3">8) порядок покрытия членами потребительского кооператива убытков, понесенных
        кооперативом;</p>
      <p class="mb-2 ml-3">9) условия реорганизации и прекращения деятельности кооператива;</p>
      <p class="mb-2 ml-3">10) сведения о филиалах и представительствах;</p>
      <p class="mb-2 ml-3">11) характер и порядок личного трудового участия членов потребительского кооператива в его
        деятельности;</p>
      <p class="mb-2 ml-3">12) взаимоотношения между потребительским кооперативом и его членами, исполнительным органом
        потребительского кооператива и наемными работниками.</p>
      <p class="mb-2">2. К уставу потребительского кооператива прилагается список членов потребительского кооператива с
        указанием фамилии, имени, отчества, места жительства и данных документа, удостоверяющего личность гражданина, а
        также наименования, местонахождения и банковских реквизитов организации, если членом потребительского
        кооператива является юридическое лицо.</p>
      <p class="mb-2">3. В уставе потребительского кооператива могут содержаться и другие положения, не противоречащие
        законодательству Республики Казахстан.</p>
      <p class="mb-2">4. Изменения и дополнения в устав потребительского кооператива регистрируются уполномоченными
        государственными органами в установленном законом порядке.</p>
      <p class="mb-2">5. В отношениях с третьими лицами потребительский кооператив и его члены вправе ссылаться на
        изменения устава потребительского кооператива с момента извещения органа, осуществляющего государственную
        регистрацию, о внесенных изменениях и дополнениях в его устав или их регистрации в порядке, установленном
        законодательными актами Республики Казахстан, либо с момента внесения сведений об изменении устава
        потребительского кооператива в Национальный реестр бизнес-идентификационных номеров на основании решения суда.
        Однако третьи лица вправе действовать с учетом этих изменений и до наступления указанных обстоятельств и
        сроков.</p>
      <p class="mb-4">Сноска. Статья 11 с изменением, внесенным законами РК от 27.04.2012 № 15-V (вводится в действие по
        истечении десяти календарных дней после его первого официального опубликования); от 24.12.2012 № 60-V (вводится
        в действие по истечении десяти календарных дней после его первого официального опубликования).</p>
