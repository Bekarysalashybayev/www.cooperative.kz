      <strong><p class="mb-4">Глава 3. Членство в потребительском кооперативе</p></strong>
      <strong><p class="mb-2">Статья 13. Членство в потребительском кооперативе</p></strong>
      <p class="mb-2">1. Членами потребительского кооператива могут быть граждане, достигшие восемнадцатилетнего
        возраста, и юридические лица.</p>
      <p class="mb-2">2. Граждане и юридические лица могут быть членами нескольких потребительских кооперативов.</p>
      <p class="mb-2">3. Количество членов потребительского кооператива должно быть не менее двух граждан. Максимальное
        число членов потребительского кооператива не ограничивается.</p>
      <p class="mb-2">4. Прием в потребительский кооператив новых членов, произведенный с соблюдением положений
        настоящего Закона и устава потребительского кооператива, оформляется решением общего собрания.</p>
      <p class="mb-2">5. Гражданин или юридическое лицо, желающие стать членом потребительского кооператива, подают в
        исполнительный орган заявление в письменной форме о приеме в потребительский кооператив. В заявлении должны быть
        указаны фамилия, имя, отчество, место жительства и данные документа, удостоверяющего личность, гражданина или
        наименование, местонахождение и банковские реквизиты юридического лица.</p>
      <p class="mb-2">6. Заявление о приеме в потребительский кооператив должно быть рассмотрено в течение десяти дней
        исполнительным органом потребительского кооператива.</p>
      <p class="mb-2">7. Третьи лица, получившие право на долю в имуществе потребительского кооператива после его
        создания, имеют первоочередное право на принятие в члены потребительского кооператива.</p>
      <p class="mb-4">8. В случае смерти члена потребительского кооператива его наследники имеют первоочередное право на
        принятие в члены потребительского кооператива, если иное не предусмотрено уставом кооператива. В последнем
        случае потребительский кооператив выплачивает наследникам долю в имуществе потребительского кооператива,
        пропорциональную его имущественному (паевому) взносу.</p>
      <strong><p class="mb-2">Статья 14. Права членов потребительского кооператива</p></strong>
      <p class="mb-2">Члены потребительского кооператива вправе:</p>
      <p class="mb-2">1) добровольно выйти в установленном порядке из потребительского кооператива;</p>
      <p class="mb-2">2) участвовать в деятельности потребительского кооператива, избирать и быть избранными в органы
        управления и органы контроля, вносить предложения об улучшении деятельности потребительского кооператива,
        устранении недостатков в работе его органов;</p>
      <p class="mb-2">3) приобретать (получать) преимущественно перед другими гражданами и юридическими лицами товары
        (услуги) потребительского кооператива, осуществлять на основе договоров услуг по сбыту изделий и продукции
        собственного производства, личного подсобного хозяйства и промысла через организации потребительского
        кооператива;</p>
      <p class="mb-2">4) пользоваться льготами, предусмотренными для членов потребительского кооператива общим собранием
        потребительского кооператива;</p>
      <p class="mb-2">5) получать от исполнительного, контрольного и иных органов потребительского кооператива
        информацию об их деятельности, в том числе знакомиться с данными бухгалтерского учета, отчетности и другой
        документацией в порядке, определяемом уставом;</p>
      <p class="mb-2">6) обращаться к общему собранию потребительского кооператива с жалобами на неправомерные действия
        его исполнительного, контрольного и иных органов;</p>
      <p class="mb-2">7) обжаловать в судебном порядке решения исполнительного, контрольного и иных органов
        потребительского кооператива, затрагивающие их интересы;</p>
      <p class="mb-2">8) получать в случае ликвидации потребительского кооператива стоимость части имущества,
        оставшегося после расчетов с кредиторами, или, по соглашению большинства членов потребительского кооператива,
        часть этого имущества в натуре, пропорциональную его имущественному (паевому) взносу;</p>
      <p class="mb-2">9) выделять свою долю в имуществе потребительского кооператива и получать ее стоимость или, по
        соглашению большинства членов потребительского кооператива, имущество в натуре, пропорциональное его
        имущественному (паевому) взносу, в случае выхода из него;</p>
      <p class="mb-4">10) получать вознаграждение за свой труд в потребительском кооперативе в форме заработной платы
        или иной форме, определенной уставом.
        Уставом потребительского кооператива могут быть установлены и иные права членов, не противоречащие
        законодательству Республики Казахстан.</p>
      <strong>
        <p class="mb-2">Статья 15. Обязанности членов потребительского кооператива</p>
      </strong>
      <p class="mb-2">Члены потребительского кооператива обязаны:</p>
      <p class="mb-2">1) соблюдать устав;</p>
      <p class="mb-2">2) выполнять решения общего собрания, исполнительного, контрольного и иных органов;</p>
      <p class="mb-2">3) выполнять свои обязательства перед потребительским кооперативом по участию в его деятельности,
        определенные уставом;</p>
      <p class="mb-4">4) вносить имущественные (паевые) взносы в размерах, порядке и в сроки, установленных уставом.
        Члены потребительского кооператива могут нести и другие обязанности, предусмотренные уставом потребительского
        кооператива, не противоречащие законодательству Республики Казахстан.</p>
      <strong><p class="mb-2">Статья 16. Прекращение членства в потребительском кооперативе</p></strong>
      <p class="mb-2">1. Членство в потребительском кооперативе прекращается в случаях:</p>
      <p class="mb-2 ml-3">1) добровольного выхода;</p>
      <p class="mb-2 ml-3">2) исключения;</p>
      <p class="mb-2 ml-3">3) утраты права на долю в имуществе;</p>
      <p class="mb-2 ml-3">4) смерти гражданина, являющегося членом потребительского кооператива, объявления его
        умершим, признания безвестно отсутствующим в установленном законодательством Республики Казахстан порядке;</p>
      <p class="mb-2 ml-3">5) ликвидации юридического лица, являющегося членом потребительского кооператива;</p>
      <p class="mb-2 ml-3">6) ликвидации потребительского кооператива.</p>
      <p class="mb-2">2. В случае добровольного выхода член потребительского кооператива имеет право на выдел своей доли
        в имуществе потребительского кооператива, пропорциональной внесенному имущественному (паевому) взносу, в
        порядке, установленном настоящим Законом.</p>
      <p class="mb-2">3. Член потребительского кооператива может быть исключен из потребительского кооператива решением
        общего собрания в случае неисполнения им без уважительных причин своих обязанностей, установленных уставом, либо
        совершения умышленных действий, наносящих ущерб потребительскому кооперативу.</p>
      <p class="mb-2">Член потребительского кооператива должен быть извещен в письменной форме не позднее чем за десять
        дней исполнительным органом потребительского кооператива о причинах вынесения на общее собрание вопроса о его
        исключении из потребительского кооператива и приглашен на указанное общее собрание, на котором ему должно быть
        предоставлено право высказать свое мнение.
        В случае отсутствия исключаемого члена потребительского кооператива без уважительной причины на общем собрании
        потребительского кооператива оно вправе принять решение о его исключении без его участия. Решение считается
        принятым, если за него проголосовало две трети присутствующих на собрании.
        Исключенному члену потребительского кооператива производится выдел доли в имуществе потребительского
        кооператива, пропорциональной внесенному имущественному (паевому) взносу, в порядке, установленном настоящим
        Законом.
        Решение общего собрания об исключении члена потребительского кооператива из кооператива может быть обжаловано в
        судебном порядке.</p>
      <p class="mb-2">4. Член потребительского кооператива, утративший право на долю в имуществе потребительского
        кооператива, прекращает свое членство в потребительском кооперативе.
        Утрата доли происходит по следующим основаниям:</p>
      <p class="mb-2 ml-3">1) продажи;</p>
      <p class="mb-2 ml-3">2) дарения;</p>
      <p class="mb-2 ml-3">3) распоряжения иным образом;</p>
      <p class="mb-2 ml-3">4) решения суда.</p>
      <p class="mb-2">Член потребительского кооператива, утративший право на долю в имуществе, информирует об этом
        исполнительный орган потребительского кооператива. Данную информацию исполнительному органу потребительского
        кооператива могут представить лица, получившие право на долю в имуществе.
        Прекращение членства в потребительском кооперативе в случае утраты членом права на долю в имуществе оформляется
        решением исполнительного органа потребительского кооператива.</p>
      <p class="mb-2">5. В случае смерти, признания безвестно отсутствующим или объявления умершим в установленном
        порядке гражданина, являющегося членом потребительского кооператива, членство его в потребительском кооперативе
        прекращается.
        Исполнительный орган потребительского кооператива производит выдел доли умершего члена потребительского
        кооператива в имуществе на день смерти, признания безвестно отсутствующим или объявления его умершим,
        пропорциональной его доле, в порядке, установленном настоящим Законом, для выплаты ее наследникам.</p>
      <p class="mb-4">6. Обо всех случаях прекращения членства в потребительском кооперативе исполнительный орган
        информирует членов потребительского кооператива.</p>
      <strong><p class="mb-4">Глава 4. Управление потребительским кооперативом</p></strong>
      <strong><p class="mb-2">Статья 17. Органы управления потребительского кооператива</p></strong>
      <p class="mb-2">1. Органами управления потребительского кооператива являются:</p>
      <p class="mb-2 ml-3">1) высший орган - общее собрание;</p>
      <p class="mb-2 ml-3">2) исполнительный орган - правление (председатель);</p>
      <p class="mb-2 ml-3">3) контрольный орган - ревизионная комиссия (ревизор).</p>
      <p class="mb-2">2. Уставом потребительского кооператива может быть предусмотрено создание иных органов
        потребительского кооператива.</p>
      <p class="mb-2">3. В случаях признания потребительского кооператива банкротом или применения реабилитационной
        процедуры и назначения временного либо банкротного или реабилитационного управляющего в порядке, установленном
        законодательным актом Республики Казахстан о реабилитации и банкротстве, все полномочия по управлению им
        переходят соответственно к временному либо банкротному или реабилитационному управляющему.</p>
      <p class="mb-4">Сноска. Статья 17 с изменением, внесенным Законом РК от 07.03.2014 № 177-V (вводится в действие по
        истечении десяти календарных дней после дня его первого официального опубликования).</p>
      <strong><p class="mb-2">Статья 18. Общее собрание потребительского кооператива</p></strong>
      <p class="mb-2">1. Общее собрание потребительского кооператива является его высшим органом и полномочно решать все
        вопросы, касающиеся его деятельности. При этом могут быть созваны очередные или внеочередные общие собрания,
        которые признаются правомочными, если на них присутствует более половины всех членов потребительского
        кооператива. Решение общего собрания считается принятым, если за него проголосовало более половины членов,
        присутствующих на собрании. При этом каждый член потребительского кооператива при принятии решений общего
        собрания имеет один голос, независимо от величины его доли в имуществе потребительского кооператива.</p>
      <p class="mb-2">2. Очередное общее собрание потребительского кооператива созывается исполнительным органом в
        сроки, установленные уставом, но не реже одного раза в год.</p>
      <p class="mb-2">Общее собрание, посвященное утверждению годовой финансовой отчетности потребительского
        кооператива, должно быть проведено не позднее трех месяцев после окончания отчетного финансового года.</p>
      <p class="mb-2">3. Внеочередное общее собрание потребительского кооператива созывается в случаях, предусмотренных
        настоящим Законом, уставом, а также в любых иных случаях, когда созыва такого собрания требуют интересы
        потребительского кооператива.</p>
      <p class="mb-2">Внеочередное общее собрание потребительского кооператива созывается исполнительным органом по
        собственной инициативе, а также по требованию ревизионной комиссии (ревизора) либо по инициативе не менее одной
        десятой части от общего количества членов потребительского кооператива.</p>
      <p class="mb-2">Если несмотря на требования ревизионной комиссии (ревизора) или членов потребительского
        кооператива исполнительный орган не созывает внеочередное общее собрание, оно может быть созвано ревизионной
        комиссией (ревизором) или не менее одной десятой части от общего количества членов потребительского кооператива
        самостоятельно.</p>
      <p class="mb-2">Внеочередное общее собрание потребительского кооператива, находящегося в процессе ликвидации,
        может также созываться ликвидационной комиссией.</p>
      <p class="mb-2">4. Решение общего собрания потребительского кооператива, принятое с нарушением порядка проведения
        общего собрания, и принятие решений, установленных настоящим Законом, уставом или правилами и иными документами,
        регулирующими внутреннюю деятельность потребительского кооператива, равно как и решение общего собрания,
        противоречащее настоящему Закону либо уставу, в том числе решение, нарушающее права членов потребительского
        кооператива, могут быть признаны судом недействительными полностью или частично по заявлению членов
        потребительского кооператива. Такое заявление может быть подано в течение шести месяцев со дня проведения общего
        собрания.</p>
      <p class="mb-4">5. Порядок созыва и проведения, принятия решения определяется уставом потребительского
        кооператива.</p>
      <strong><p class="mb-2">Статья 19. Компетенция общего собрания потребительского кооператива</p></strong>
      <p class="mb-2">1. Компетенция общего собрания потребительского кооператива определяется в соответствии с
        настоящим Законом и уставом потребительского кооператива.</p>
      <p class="mb-2">2. К исключительной компетенции общего собрания относятся:</p>
      <p class="mb-2 ml-3">1) внесение изменений и дополнений в устав потребительского кооператива, включая изменения о
        размере имущественного (паевого) взноса, о месте нахождения и наименовании, или утверждение устава в новой
        редакции;</p>
      <p class="mb-2 ml-3">2) определение основных направлений деятельности потребительского кооператива;</p>
      <p class="mb-2 ml-3">3) избрание руководителя и членов исполнительного, контрольного и иных органов
        потребительского кооператива, прекращение их полномочий, заслушивание отчетов об их деятельности, определение
        размера средств на их содержание;</p>
      <p class="mb-2 ml-3">4) исключение членов потребительского кооператива из кооператива;</p>
      <p class="mb-2 ml-3">5) утверждение внутренних правил, процедуры их принятия и других документов, регулирующих
        внутреннюю деятельность потребительского кооператива;</p>
      <p class="mb-2 ml-3">6) решение вопросов о создании ассоциации (союза) потребительских кооперативов, вступлении в
        ассоциацию (союз) и выходе из нее;</p>
      <p class="mb-2 ml-3">7) утверждение годовых финансовых отчетов, отчета ревизионной комиссии (ревизора)
        потребительского кооператива;</p>
      <p class="mb-2 ml-3">8) порядок покрытия убытков, понесенных потребительским кооперативом;</p>
      <p class="mb-2 ml-3">9) определение видов и порядка формирования и расходования фондов потребительского
        кооператива;</p>
      <p class="mb-2 ml-3">10) отчуждение недвижимого имущества потребительского кооператива, стоимость которого
        превышает стоимость, определенную уставом;</p>
      <p class="mb-2 ml-3">11) принятие решений о реорганизации и ликвидации потребительского кооператива;</p>
      <p class="mb-2 ml-3">12) избрание председателя и членов ликвидационной комиссии и утверждение ликвидационного
        баланса.</p>
      <p class="mb-2">3. Наряду с вопросами, отнесенными настоящим Законом к исключительной компетенции общего собрания,
        уставом потребительского кооператива к его исключительной компетенции могут быть отнесены и другие вопросы.</p>
      <p class="mb-2">4. Общее собрание вправе, если иное не установлено уставом потребительского кооператива,
        делегировать полномочия, не относящиеся к его исключительной компетенции, исполнительному органу
        потребительского кооператива.</p>
      <p class="mb-2">5. Общее собрание потребительского кооператива имеет право принять к своему рассмотрению вопросы
        по подтверждению или отмене решений исполнительного, контрольного и иных органов.</p>
      <p class="mb-4">6. Общее собрание потребительского кооператива вправе принять к рассмотрению любой вопрос,
        связанный с деятельностью потребительского кооператива.</p>
      <strong><p class="mb-2">Статья 20. Исполнительный орган потребительского кооператива</p></strong>
      <p class="mb-2">1. Исполнительный орган потребительского кооператива осуществляет текущее руководство
        деятельностью потребительского кооператива и ведение его дел и подотчетен общему собранию.</p>
      <p class="mb-2">Члены исполнительного органа потребительского кооператива избираются в составе и на срок,
        определенный уставом, и могут быть освобождены от исполнения обязанностей (полномочий) в любое время по решению
        общего собрания потребительского кооператива.</p>
      <p class="mb-2">2. Порядок работы и принятия решений исполнительного органа определяется уставом потребительского
        кооператива.</p>
      <p class="mb-2">3. К компетенции исполнительного органа относятся все вопросы обеспечения деятельности
        потребительского кооператива, не относящиеся к компетенции общего собрания, определенные настоящим Законом и
        уставом кооператива.</p>
      <p class="mb-2">К компетенции исполнительного органа потребительского кооператива относятся также полномочия
        общего собрания, не относящиеся к его исключительной компетенции, переданные исполнительному органу в
        соответствии с пунктом 4 статьи 19 настоящего Закона.</p>
      <p class="mb-2">4. В отношениях с третьими лицами потребительский кооператив не вправе ссылаться на установленные
        им ограничения полномочий исполнительного органа. Однако потребительский кооператив вправе оспаривать
        действительность сделки, совершенной его исполнительным органом, с третьим лицом с нарушением установленных
        ограничений, если докажет, что в момент заключения сделки третье лицо знало или заведомо должно было знать о
        таких ограничениях.</p>
      <p class="mb-2">5. Членам исполнительного органа потребительского кооператива запрещается:</p>
      <p class="mb-2 ml-3">1) без согласия общего собрания заключать с потребительским кооперативом сделки, направленные
        на получение от него имущественных выгод (включая договоры дарения, займа, безвозмездного пользования,
        купли-продажи и другое);</p>
      <p class="mb-2 ml-3">2) получать комиссионное вознаграждение как от самого потребительского кооператива, так и от
        третьих лиц за сделки, заключенные потребительским кооперативом с третьими лицами;</p>
      <p class="mb-2 ml-3">3) выступать от имени или в интересах третьих лиц в их отношениях с потребительским
        кооперативом;</p>
      <p class="mb-2 ml-3">4) осуществлять предпринимательскую деятельность, конкурирующую с деятельностью данного
        потребительского кооператива.</p>
      <p class="mb-4">6. Ограничения, предусмотренные подпунктами 1)-3) пункта 5 настоящей статьи, распространяются
        также на близких родственников и свойственников - членов исполнительного органа потребительского
        кооператива.</p>
      <strong><p class="mb-2">Статья 21. Наблюдательный совет потребительского кооператива</p></strong>
      <p class="mb-2">1. Наблюдательный совет потребительского кооператива может быть создан, если это предусмотрено
        уставом потребительского кооператива в целях контроля за деятельностью исполнительного органа потребительского
        кооператива со стороны его членов. Полномочия наблюдательного совета определяются уставом.</p>
      <p class="mb-2">2. Наблюдательный совет потребительского кооператива состоит из его членов, избираемых общим
        собранием членов потребительского кооператива. Количество членов наблюдательного совета и порядок принятия им
        решений устанавливаются уставом потребительского кооператива.</p>
      <p class="mb-4">3. Член наблюдательного совета не может быть одновременно членом исполнительного органа,
        ревизионной комиссии потребительского кооператива либо председателем потребительского кооператива.</p>
      <strong><p class="mb-2">Статья 22. Ревизионная комиссия (ревизор) потребительского кооператива</p></strong>
      <p class="mb-2">1. Для осуществления контроля за финансово-хозяйственной деятельностью потребительского
        кооператива должна быть образована ревизионная комиссия (ревизор) из числа членов потребительского кооператива,
        если иное не предусмотрено уставом.</p>
      <p class="mb-2">Исполнение функций ревизионной комиссии может быть поручено одному из членов потребительского
        кооператива в качестве ревизора.</p>
      <p class="mb-2">2. Ревизионная комиссия или ревизор потребительского кооператива избирается общим собранием на
        срок, определенный в уставе.</p>
      <p class="mb-2">3. Членами ревизионной комиссии (ревизором) не могут быть члены исполнительного органа
        потребительского кооператива.</p>
      <p class="mb-2">4. Ревизионная комиссия (ревизор) потребительского кооператива подотчетна общему собранию.</p>
      <p class="mb-2">5. Ревизионная комиссия (ревизор) вправе производить проверки финансово-хозяйственной деятельности
        потребительского кооператива. Ревизионная комиссия (ревизор) обладает для этой цели правом безусловного доступа
        в любое время ко всей документации потребительского кооператива. По требованию ревизионной комиссии (ревизора)
        исполнительный орган обязан давать необходимые пояснения в устной или письменной форме.</p>
      <p class="mb-2">6. Ревизионная комиссия (ревизор) в обязательном порядке проводит проверку годовой финансовой
        отчетности потребительского кооператива до их утверждения общим собранием. Общее собрание не вправе утверждать
        годовую финансовую отчетность без заключения ревизионной комиссии (ревизора).</p>
      <p class="mb-4">7. Порядок работы ревизионной комиссии (ревизора) определяется уставом, а также правилами и иными
        документами, регулирующими внутреннюю деятельность потребительского кооператива.</p>
      <strong><p class="mb-2">Статья 23. Аудит потребительского кооператива</p></strong>
      <p class="mb-2">1. Для проверки и подтверждения правильности годовой финансовой отчетности потребительского
        кооператива, а также текущего состояния его дел потребительский кооператив вправе в случаях и порядке,
        предусмотренных в его уставе, привлекать аудиторскую организацию, не связанную имущественными интересами с
        потребительским кооперативом, членами его исполнительного, контрольного и иных органов или членами
        потребительского кооператива.</p>
      <p class="mb-2">2. Член потребительского кооператива вправе требовать проведения за свой счет аудита финансовой
        отчетности кооператива.</p>
      <p class="mb-2">3. Если исполнительный орган потребительского кооператива уклоняется от проведения аудита
        финансовой отчетности кооператива, когда аудит обязателен либо когда его проведения требует член кооператива,
        аудит может быть назначен решением суда, принятым по заявлению заинтересованного лица либо члена
        кооператива.</p>
      <p class="mb-4">Сноска. В статью 23 внесены изменения Законом РК от 5 мая 2006 года N 139 (порядок введения в
        действие см. ст.2 Закона РК N 139).</p>
      <strong><p class="mb-2">Статья 24. Обжалование решений и действий исполнительного, контрольного и иных органов
        потребительского кооператива</p></strong>
      <p class="mb-2">Решения и действия исполнительного, контрольного и иных органов потребительского кооператива могут
        быть обжалованы членами потребительского кооператива на общем собрании или в судебном порядке.</p>
      <p class="mb-4">Решения и действия руководителя исполнительного органа потребительского кооператива могут быть
        обжалованы на общем собрании потребительского кооператива или в суде.</p>
      <strong><p class="mb-4">Глава 5. Имущество потребительского кооператива</p></strong>
      <strong><p class="mb-2">Статья 25. Имущество потребительского кооператива, источники его формирования</p></strong>
      <p class="mb-2">1. Собственником имущества, числящегося на балансе потребительского кооператива, является
        потребительский кооператив как юридическое лицо.</p>
      <p class="mb-2">Имущество потребительского кооператива образуется путем объединения его членами имущественных
        (паевых) взносов.</p>
      <p class="mb-2">Кроме имущественных (паевых) и дополнительных взносов, источниками формирования имущества
        потребительского кооператива являются доходы, получаемые от предпринимательской деятельности потребительского
        кооператива и иные источники, не запрещенные законодательством Республики Казахстан.</p>
      <p class="mb-2">2. Доходы, полученные потребительским кооперативом, не могут распределяться между его членами и
        направляются на уставные цели.</p>
      <p class="mb-4">3. Распоряжение имуществом, находящимся в собственности потребительского кооператива,
        осуществляется по решению общего собрания.</p>
      <strong><p class="mb-2">Статья 26. Имущественный (паевой) взнос</p></strong>
      <p class="mb-2">1. Имущественный (паевой) взнос может быть внесен деньгами, ценными бумагами, вещами,
        имущественными правами, в том числе правом землепользования и правом на результаты интеллектуальной деятельности
        и иное имущество. Не допускается внесение взносов в виде личных неимущественных прав и иных нематериальных
        благ.</p>
      <p class="mb-2">2. Взносы в натуральной форме или в виде имущественных прав оцениваются в денежной форме по
        соглашению всех членов потребительского кооператива или по решению общего собрания потребительского
        кооператива.</p>
      <p class="mb-2">3. В случаях, когда в качестве вклада потребительскому кооперативу передается право пользования
        имуществом, размер этого вклада определяется платой за пользование, исчисленной за весь срок, определенный общим
        собранием потребительского кооператива.</p>
      <p class="mb-2">Без согласия общего собрания потребительского кооператива досрочное изъятие имущества, право
        пользования которым является имущественным (паевым) вкладом, не допускается.</p>
      <p class="mb-2">Если иное не предусмотрено уставом, риск случайной гибели или случайной порчи имущества,
        переданного в пользование потребительскому кооперативу, возлагается на собственника имущества.</p>
      <p class="mb-4">4. Сроки внесения имущественных (паевых) взносов определяются уставом потребительского
        кооператива.</p>
      <strong><p class="mb-2">Статья 27. Фонды потребительского кооператива</p></strong>
      <p class="mb-2">1. При осуществлении своей деятельности потребительский кооператив вправе формировать следующие
        фонды:</p>
      <p class="mb-2 ml-3">1) имущественный (паевой);</p>
      <p class="mb-2 ml-3">2) развития потребительского кооператива;</p>
      <p class="mb-2 ml-3">3) резервный;</p>
      <p class="mb-2 ml-3">4) стимулирования и социальной поддержки членов потребительского кооператива;</p>
      <p class="mb-2 ml-3">5) иные фонды в соответствии с уставом.</p>
      <p class="mb-2">2. Имущественный (паевой) фонд состоит из имущественных (паевых) взносов членов потребительского
        кооператива и является одним из источников формирования имущества потребительского кооператива.</p>
      <p class="mb-2">3. Источником формирования других фондов являются доходы, полученные потребительским кооперативом,
        и иные источники, не запрещенные законодательством Республики Казахстан.</p>
      <p class="mb-4">4. Порядок формирования и использования фондов, их перераспределение устанавливаются общим
        собранием потребительского кооператива.</p>
      <strong><p class="mb-2">Статья 28. Доли членов потребительского кооператива в имуществе</p></strong>
      <p class="mb-2">1. Доли всех членов потребительского кооператива в имуществе (в стоимости имущества)
        потребительского кооператива определяются пропорционально их вкладам в имущество потребительского
        кооператива.</p>
      <p class="mb-2">2. Уставом потребительского кооператива или общим собранием может быть установлен порядок
        определения и изменения долей в зависимости от вклада каждого из них в образование и приращение имущества.</p>
      <p class="mb-2">3. Каждый член потребительского кооператива вправе по своему усмотрению продать, подарить,
        завещать, заложить свою долю либо распорядиться ею иным образом с соблюдением условий, предусмотренных настоящим
        Законом и уставом.</p>
      <p class="mb-4">4. Перед совершением каких-либо операций со своей долей в имуществе член потребительского
        кооператива обязан подать заявление в исполнительный орган в сроки, определенные уставом потребительского
        кооператива, с требованием выдела своей доли из общего имущества.</p>
      <strong>
        <p class="mb-2">Статья 29. Распоряжение членом потребительского кооператива своей долей в имуществе</p>
      </strong>
      <p class="mb-2">1. Доля члена потребительского кооператива в имуществе потребительского кооператива может быть
        отчуждена или заложена до полной оплаты имущественного (паевого) взноса лишь в той части, в которой взнос уже
        оплачен.</p>
      <p class="mb-2">2. Член потребительского кооператива вправе продать или иным способом уступить свою долю в
        имуществе потребительского кооператива или ее часть одному или нескольким членам потребительского кооператива по
        своему выбору. Равным образом член потребительского кооператива вправе заложить долю в обеспечение своего
        обязательства перед другим членом. Согласия потребительского кооператива или других членов потребительского
        кооператива на совершение этих сделок не требуется.</p>
      <p class="mb-2">3. Отчуждение членом потребительского кооператива своей доли (ее части) третьим лицам или залог
        доли (части доли) в обеспечение обязательства члена потребительского кооператива перед третьим лицом
        допускается.</p>
      <p class="mb-4">4. Уставом потребительского кооператива может быть предусмотрено, что продажа доли третьему лицу
        допустима лишь с соблюдением определенных условий.</p>
      <strong><p class="mb-2">Статья 30. Преимущественное право покупки отчуждаемой доли</p></strong>
      <p class="mb-2">1. Член потребительского кооператива пользуется преимущественным перед третьими лицами правом
        покупки доли члена потребительского кооператива или ее части при ее продаже кем-либо из членов потребительского
        кооператива. Таким правом может воспользоваться каждый член потребительского кооператива. Если членов
        потребительского кооператива, желающих воспользоваться преимущественным правом покупки, несколько и уставом или
        иным соглашением членов потребительского кооператива не предусмотрено иное, преимущественное право покупки доли
        (ее части) осуществляется членами потребительского кооператива пропорционально размерам их долей в имуществе
        потребительского кооператива.</p>
      <p class="mb-2">2. В течение семи дней с момента получения от члена потребительского кооператива извещения о
        предложении доли к продаже исполнительный орган извещает об этом всех членов потребительского кооператива. Член
        потребительского кооператива, желающий осуществить преимущественное право покупки, должен в семидневный срок
        уведомить об этом исполнительный орган, указав при этом, что намерен приобрести предлагаемую к продаже долю
        полностью или в определенной части.</p>
      <p class="mb-2">3. Если совокупная величина поступивших предложений не превышает размера продаваемой доли, каждый
        из членов потребительского кооператива приобретает ту ее часть, которую он указал в своем уведомлении.
        Оставшаяся часть доли может быть отчуждена третьему лицу, если до такого отчуждения не поступит дополнительных
        предложений от членов потребительского кооператива.</p>
      <p class="mb-2">4. При продаже доли или ее части с нарушением преимущественного права покупки любой член
        потребительского кооператива может в течение трех месяцев обжаловать данное нарушение в судебном порядке.</p>
      <p class="mb-2">5. Преимущественное право покупки отчуждаемой доли осуществляется при любом способе продажи доли,
        в том числе на торгах.</p>
      <p class="mb-2">6. Уступка преимущественного права покупки доли не допускается.</p>
      <p class="mb-2">7. В случае приобретения отчуждаемой доли или ее части членом (членами) потребительского
        кооператива его доля в имуществе кооператива увеличивается.</p>
      <p class="mb-2">8. Правила настоящей статьи применяются также при отчуждении доли по договору мены.</p>
      <p class="mb-4">9. При нежелании членов потребительского кооператива воспользоваться преимущественным правом
        покупки доли или ее части при ее продаже третьему лицу преимущественным правом покупки может воспользоваться сам
        потребительский кооператив.</p>
      <strong>
        <p class="mb-2">
          Статья 31. Продажа доли члена потребительского кооператива при отказе других членов от покупки доли
        </p>
      </strong>
      <p class="mb-2">1. Уставом потребительского кооператива может быть предусмотрено запрещение или ограничение
        продажи членом потребительского кооператива своей доли третьим лицам.</p>
      <p class="mb-2">2. В случае, когда продажа доли по обстоятельствам, не зависящим от продавца, не может быть
        произведена с соблюдением запрещений или ограничений, предусмотренных пунктом 1 настоящей статьи, член
        потребительского кооператива, желающий продать долю, вправе обратиться к потребительскому кооперативу с
        требованием выкупить эту долю или разрешить ее продажу третьему лицу.</p>
      <p class="mb-2">Выбор одного из этих вариантов производится общим собранием членов потребительского
        кооператива.</p>
      <p class="mb-2">3. При выкупе доли потребительским кооперативом цена доли определяется соглашением сторон, а при
        недостижении соглашения - судом.</p>
      <p class="mb-4">4. При согласии потребительского кооператива на продажу доли третьему лицу члены потребительского
        кооператива сохраняют преимущественное право покупки доли.</p>
      <strong>
        <p class="mb-2">
          Статья 32. Последствия выкупа потребительским кооперативом доли члена потребительского кооператива
        </p>
      </strong>
      <p class="mb-2">1. После выкупа потребительским кооперативом доли членов потребительского кооператива
        потребительский кооператив обязан предложить другим членам потребительского кооператива приобрести эту долю по
        цене, определенной решением общего собрания.</p>
      <p class="mb-2">2. В случае, когда намерение приобрести долю выражают несколько членов потребительского
        кооператива, доля делится между ними пропорционально размеру их долей в имуществе потребительского
        кооператива.</p>
      <p class="mb-2">Размер доли, выкупленной членом потребительского кооператива, добавляется к тому размеру доли,
        которая принадлежала данному члену до выкупа. При этом соблюдается возможность ограничения размера доли, которая
        может принадлежать одному члену потребительского кооператива.</p>
      <p class="mb-4"> . Потребительский кооператив по решению общего собрания вправе продать выкупленную долю от имени
        потребительского кооператива третьему лицу.</p>
      <strong>
        <p class="mb-2">
          Статья 33. Выдел доли члену потребительского кооператива, подлежащему исключению из потребительского
          кооператива
        </p>
      </strong>
      <p class="mb-2">1. Выдел доли члену потребительского кооператива, подлежащему исключению из потребительского
        кооператива, производится по решению общего собрания. </p>
      <p class="mb-2">2. При причинении членом потребительского кооператива умышленного вреда потребительскому
        кооперативу или его членам они вправе требовать от причинителя возмещения вреда за счет выделенной доли. </p>
      <p class="mb-4">3. В случае, если стороны не пришли к согласию о размерах возмещаемого вреда и выделяемой доли
        члену потребительского кооператива, подлежащему исключению, споры решаются в судебном порядке. </p>
      <strong>
        <p class="mb-2">
          Статья 34. Имущественная ответственность потребительского кооператива и его членов
        </p>
      </strong>
      <p class="mb-2">1. Потребительский кооператив отвечает по своим обязательствам всем принадлежащим ему имуществом и
        не отвечает по другим обязательствам членов потребительского кооператива.</p>
      <p class="mb-2">2. Члены потребительского кооператива обязаны в течение трех месяцев после утверждения ежегодного
        баланса покрыть образовавшиеся убытки путем дополнительных взносов.</p>
      <p class="mb-4">3. Субсидиарная ответственность членов потребительского кооператива по обязательствам
        потребительского кооператива определяется в порядке, предусмотренном гражданским законодательством Республики
        Казахстан.</p>
      <strong><p class="mb-4">Глава 6. Реорганизация и ликвидация потребительского кооператива</p></strong>
      <strong><p class="mb-2">Статья 35. Реорганизация потребительского кооператива</p></strong>
      <p class="mb-2">1. Реорганизация потребительского кооператива (слияние, присоединение, разделение, выделение)
        может осуществляться добровольно по решению общего собрания. Отчуждение доли или иное изменение состава членов
        потребительского кооператива не является реорганизацией потребительского кооператива.</p>
      <p class="mb-2">2. В случаях, установленных законодательными актами, принудительная реорганизация потребительского
        кооператива в форме его разделения или выделения из его состава одного или нескольких потребительских
        кооперативов осуществляется по решению суда.</p>
      <p class="mb-4">3. Потребительский кооператив не может быть преобразован в другие виды и формы юридических
        лиц.</p>
      <strong><p class="mb-2">Статья 36. Слияние, присоединение потребительских кооперативов</p></strong>
      <p class="mb-2">1. Слияние двух или нескольких потребительских кооперативов осуществляется путем полного
        объединения их имущества. В результате слияния возникает новый потребительский кооператив, а сливающиеся
        потребительские кооперативы прекращают свою деятельность. При этом все права и обязанности каждого из
        участвующих в слиянии потребительских кооперативов переходят к вновь возникшему потребительскому кооперативу в
        соответствии с передаточным актом.</p>
      <p class="mb-2">2. Присоединение одного или нескольких потребительских кооперативов к другому потребительскому
        кооперативу осуществляется путем включения имущества присоединяемых потребительских кооперативов в имущество
        присоединяющего потребительского кооператива. При этом присоединяемые потребительские кооперативы прекращают
        свою деятельность, а все их права и обязанности переходят в соответствии с передаточным актом к присоединяющему
        потребительскому кооперативу, в устав которого вносятся связанные с реорганизацией изменения и дополнения.</p>
      <p class="mb-2">3. Исполнительные органы потребительских кооперативов, участвующих в слиянии, присоединении,
        готовят проект договора о слиянии, присоединении и выносят на рассмотрение общего собрания каждого
        потребительского кооператива вопросы о слиянии, присоединении и утверждение договора о слиянии,
        присоединении.</p>
      <p class="mb-2">Согласованный текст договора о слиянии, присоединении подписывается уполномоченными на то
        исполнительными органами потребительских кооперативов.</p>
      <p class="mb-2">Договор о слиянии, присоединении должен содержать сведения о наименовании, месте нахождения и
        адресе каждого из участвующих в слиянии, присоединении потребительских кооперативов, основные данные их
        балансов, а также предусматривать порядок и условия слияния, присоединения.</p>
      <p class="mb-2">4. Каждый участвующий в слиянии, присоединении потребительский кооператив обязан в двухмесячный
        срок со дня принятия общим собранием решения о слиянии, присоединении направить всем своим кредиторам письменные
        уведомления о слиянии, присоединении и поместить соответствующее объявление в официальных органах печати. К
        уведомлению (объявлению) прилагаются сведения о других участвующих в слиянии, присоединении потребительских
        кооперативах, указанные в пункте 3 настоящей статьи.</p>
      <p class="mb-2">Кредиторы потребительского кооператива вправе в двухмесячный срок со дня получения уведомления или
        публикации объявления потребовать от потребительского кооператива дополнительных гарантий либо досрочного
        прекращения или исполнения потребительским кооперативом соответствующих обязательств и возмещения убытков.
        Требования направляются потребительскому кооперативу в письменной форме.</p>
      <p class="mb-2">5. Каждый из участвующих в слиянии, присоединении потребительских кооперативов обязан с момента
        принятия общим собранием решения о слиянии, присоединении сообщать об этом решении кредиторам по обязательствам,
        возникающим после принятия решения.</p>
      <p class="mb-4">На основании договора о слиянии, присоединении потребительских кооперативов члены сливающихся,
        присоединяющихся потребительских кооперативов на общем собрании принимают устав вновь образуемого
        потребительского кооператива и избирают исполнительный, контрольный и иные органы.</p>
      <strong><p class="mb-2">Статья 37. Разделение, выделение потребительского кооператива</p></strong>
      <p class="mb-2">1. Разделение потребительского кооператива осуществляется путем деления имущества этого
        потребительского кооператива между двумя или несколькими возникающими потребительскими кооперативами. При этом
        права и обязанности разделяемого потребительского кооператива переходят к вновь возникающим потребительским
        кооперативам в соответствии с разделительным балансом.</p>
      <p class="mb-2">2. Выделение из потребительского кооператива одного или нескольких потребительских кооперативов
        осуществляется путем выделения части имущества потребительского кооператива и передачи ее одному или нескольким
        вновь возникающим потребительским кооперативам.</p>
      <p class="mb-2">При этом часть прав и обязанностей реорганизуемого потребительского кооператива переходит к вновь
        созданным потребительским кооперативам в соответствии с разделительным балансом.</p>
      <p class="mb-2">3. Исполнительный орган реорганизуемого потребительского кооператива готовит план разделения,
        выделения и проекты уставов вновь возникающих потребительских кооперативов и выносит на рассмотрение общего
        собрания вопросы о разделении, выделении потребительского кооператива, утверждении плана разделения, выделения,
        уставов вновь возникающих потребительских кооперативов и разделительного баланса, а также об избрании
        исполнительных, контрольных и иных органов вновь возникающих потребительских кооперативов.</p>
      <p class="mb-2">4. Если уставом потребительского кооператива не предусмотрено иное, при его разделении, выделении
        каждый член потребительского кооператива вправе получить долю в имуществе потребительского кооператива каждого
        из вновь возникающих потребительских кооперативов, равную его доле в имуществе реорганизуемого потребительского
        кооператива, либо получить единую долю в имуществе одного из вновь возникающих потребительских кооперативов.</p>
      <p class="mb-2">5. Потребительский кооператив обязан с момента принятия общим собранием решения о разделении,
        выделении сообщать об этом решении кредиторам по обязательствам, возникающим после принятия решения.</p>
      <p class="mb-2">6. Потребительский кооператив обязан в двухмесячный срок со дня принятия общим собранием решения о
        разделении, выделении направить всем своим кредиторам письменные уведомления о разделении, выделении и поместить
        соответствующее объявление в официальных органах печати. К уведомлению (объявлению) прилагается разделительный
        баланс, а также сведения о наименовании, месте нахождения и адресе каждого из вновь возникающих потребительских
        кооперативов.</p>
      <p class="mb-2">7. Кредиторы реорганизуемого потребительского кооператива вправе в двухмесячный срок со дня
        получения уведомления (публикации объявления) потребовать от потребительского кооператива досрочного прекращения
        или исполнения соответствующих обязательств и возмещения убытков. Требования направляются потребительскому
        кооперативу в письменной форме.</p>
      <p class="mb-4">8. Потребительские кооперативы, возникшие в результате разделения, выделения потребительского
        кооператива, несут солидарную ответственность по его обязательствам в течение года с момента регистрации новых
        потребительских кооперативов.</p>
      <strong>
        <p class="mb-2">
          Статья 38. Последствия невыполнения решения суда о принудительном разделении, выделении потребительского
          кооператива
        </p>
      </strong>
      <p class="mb-2">1. Если исполнительный орган потребительского кооператива, уполномоченный на проведение
        разделения, выделения, при принудительной реорганизации по решению суда не осуществляет разделение, выделение в
        срок, определенный в решении суда, суд назначает доверительного управляющего имуществом потребительского
        кооператива и поручает ему осуществить разделение, выделение за счет имущества реорганизуемого потребительского
        кооператива.</p>
      <p class="mb-2">2. С момента назначения доверительного управляющего к нему переходят полномочия по управлению
        потребительским кооперативом, исполнительный, контрольный и иные органы потребительского кооператива прекращают
        свою деятельность.</p>
      <p class="mb-4">3. Доверительный управляющий выступает от имени потребительского кооператива в суде, составляет
        разделительный баланс и передает его на утверждение суда вместе с учредительными документами потребительских
        кооперативов, возникающих в результате разделения, выделения. Утверждение судом указанных документов является
        основанием для государственной регистрации вновь возникающих потребительских кооперативов.</p>
      <strong>
        <p class="mb-2">
          Статья 39. Государственная регистрация юридических лиц, возникающих в результате реорганизации
          потребительского кооператива
        </p>
      </strong>
      <p class="mb-2">1. Государственная регистрация потребительского кооператива, возникающего в результате
        реорганизации, осуществляется в соответствии с законодательством Республики Казахстан о государственной
        регистрации юридических лиц и учетной регистрации филиалов и представительств. </p>
      <p class="mb-2">2. При слиянии потребительских кооперативов государственная регистрация производится
        регистрирующим органом по месту нахождения вновь возникающего потребительского кооператива. </p>
      <p class="mb-2">При присоединении потребительских кооперативов государственная регистрация производится
        регистрирующим органом по месту нахождения присоединяющего потребительского кооператива. </p>
      <p class="mb-2">При разделении, выделении потребительских кооперативов государственная регистрация производится
        регистрирующим органом по месту нахождения реорганизуемого потребительского кооператива. Данный регистрирующий
        орган сообщает сведения о государственной регистрации новых потребительских кооперативов органам, осуществляющим
        государственную регистрацию юридических лиц по месту нахождения вновь возникших потребительских
        кооперативов. </p>
      <p class="mb-2">3. Государственная регистрация потребительского кооператива, возникающего в результате
        реорганизации, производится органом, осуществляющим государственную регистрацию юридических лиц, по истечении
        срока, представленного кредиторам, для заявления требований в реорганизации потребительскому кооперативу. </p>
      <p class="mb-2">4. Если в течение года со дня, когда общим собранием последнего из участвующих в реорганизации
        потребительского кооператива было принято решение о реорганизации, заявление о государственной регистрации не
        будет подано либо не будут представлены необходимые доказательства (пункт 3 настоящей статьи), реорганизация
        считается несостоявшейся. </p>
      <p class="mb-2">5. Потребительские кооперативы, участвующие в реорганизации, за исключением случаев реорганизации
        в форме присоединения и выделения, прекращают свою деятельность с момента государственной регистрации вновь
        возникших потребительских кооперативов и исключаются из Национального реестра бизнес-идентификационных номеров.
        Присоединяемый потребительский кооператив прекращает свою деятельность с момента регистрации его присоединения к
        другому потребительскому кооперативу и исключается из Национального реестра бизнес-идентификационных
        номеров. </p>
      <p class="mb-4">Сноска. Статья 39 с изменениями, внесенными Законом РК от 24.12.2012 № 60-V (вводится в действие
        по истечении десяти календарных дней после его первого официального опубликования). </p>
      <strong>
        <p class="mb-2">
          Статья 40. Ликвидация потребительского кооператива
        </p>
      </strong>
      <p class="mb-2">1. Потребительский кооператив может быть ликвидирован по решению общего собрания или по решению
        суда в соответствии с законодательством Республики Казахстан.</p>
      <p class="mb-2">2. Общее собрание потребительского кооператива, принявшее решение по его ликвидации, назначает
        ликвидационную комиссию и устанавливает порядок и сроки ликвидации потребительского кооператива.</p>
      <p class="mb-4">3. В случае ликвидации потребительского кооператива член кооператива имеет право на выдел доли в
        имуществе кооператива, пропорциональной его имущественному (паевому) взносу.</p>
